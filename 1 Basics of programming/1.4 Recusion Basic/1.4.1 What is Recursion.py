# A function will be called again and again but with the basic condition is called the Recusion.
# Mainly there are 2 Types of Recusion
# 1. Head Recursion
# 2. Tail Recursion
# Head Recursion -> First Job is done -> called the function
# Tail Recursion -> First function is called -> job is done at last
# On each function call function will be registered in memory stack and once function is completed will be removed from the memory stack.
